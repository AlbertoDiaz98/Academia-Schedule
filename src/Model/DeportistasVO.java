package Model;

/**
 *
 * @author beto-
 */
public class DeportistasVO {

    private int idDep;
    private String nombreDep;
    private String apPaDep;
    private String apMaDep;
    private String generoDep;
    private String dia;
    private String mes;
    private String año;
    private String deporte;

    public int getIdDep() {
        return idDep;
    }

    public void setIdDep(int idDep) {
        this.idDep = idDep;
    }

    public String getNombreDep() {
        return nombreDep;
    }

    public void setNombreDep(String nombreDep) {
        this.nombreDep = nombreDep;
    }

    public String getApPaDep() {
        return apPaDep;
    }

    public void setApPaDep(String apPaDep) {
        this.apPaDep = apPaDep;
    }

    public String getApMaDep() {
        return apMaDep;
    }

    public void setApMaDep(String apMaDep) {
        this.apMaDep = apMaDep;
    }

    public String getGeneroDep() {
        return generoDep;
    }

    public void setGeneroDep(String generoDep) {
        this.generoDep = generoDep;
    }

    public String getDia() {
        return dia;
    }

    public void setDia(String dia) {
        this.dia = dia;
    }

    public String getMes() {
        return mes;
    }

    public void setMes(String mes) {
        this.mes = mes;
    }

    public String getAño() {
        return año;
    }

    public void setAño(String año) {
        this.año = año;
    }

    public String getDeporte() {
        return deporte;
    }

    public void setDeporte(String deporte) {
        this.deporte = deporte;
    }

}
