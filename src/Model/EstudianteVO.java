public class EstudianteVO {
    private int idEstu;
    private String nombreEstu;
    private String apPaEstu;
    private String apMaEstu;
    private String luNac;
    private String dia;
    private String mes;
    private String año;
    private String carrera;
    private String email;

    public int getIdEstu() {
        return idEstu;
    }

    public void setIdEstu(int idEstu) {
        this.idEstu = idEstu;
    }

    public String getNombreEstu() {
        return nombreEstu;
    }

    public void setNombreEstu(String nombreEstu) {
        this.nombreEstu = nombreEstu;
    }

    public String getApPaEstu() {
        return apPaEstu;
    }

    public void setApPaEstu(String apPaEstu) {
        this.apPaEstu = apPaEstu;
    }

    public String getApMaEstu() {
        return apMaEstu;
    }

    public void setApMaEstu(String apMaEstu) {
        this.apMaEstu = apMaEstu;
    }

    public String getLuNac() {
        return luNac;
    }

    public void setLuNac(String luNac) {
        this.luNac = luNac;
    }

    public String getDia() {
        return dia;
    }

    public void setDia(String dia) {
        this.dia = dia;
    }

    public String getMes() {
        return mes;
    }

    public void setMes(String mes) {
        this.mes = mes;
    }

    public String getAño() {
        return año;
    }

    public void setAño(String año) {
        this.año = año;
    }

    public String getCarrera() {
        return carrera;
    }

    public void setCarrera(String carrera) {
        this.carrera = carrera;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }    
    
}
